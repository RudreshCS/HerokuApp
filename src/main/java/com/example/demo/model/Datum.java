
package com.example.demo.model;

public class Datum {

	private Integer x;
	private Integer y;
	private Integer r;

	public Datum() {

	}

	public Datum(Integer x, Integer y, Integer r) {
		super();
		this.x = x;
		this.y = y;
		this.r = r;
	}

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Integer getR() {
		return r;
	}

	public void setR(Integer r) {
		this.r = r;
	}

}
